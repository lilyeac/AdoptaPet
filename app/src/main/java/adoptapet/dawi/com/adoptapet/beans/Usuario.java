package adoptapet.dawi.com.adoptapet.beans;

public class Usuario {

    private int id_usu;
    private String nom_usu;
    private String ape_usu;
    private String dir_usu;
    private String num_usu;
    private String cel_usu;
    private String email_usu;
    private String login;
    private String password;

    public Usuario(){}

    public int getId_usu() {
        return id_usu;
    }

    public void setId_usu(int id_usu) {
        this.id_usu = id_usu;
    }

    public String getNom_usu() {
        return nom_usu;
    }

    public void setNom_usu(String nom_usu) {
        this.nom_usu = nom_usu;
    }

    public String getApe_usu() {
        return ape_usu;
    }

    public void setApe_usu(String ape_usu) {
        this.ape_usu = ape_usu;
    }

    public String getDir_usu() {
        return dir_usu;
    }

    public void setDir_usu(String dir_usu) {
        this.dir_usu = dir_usu;
    }

    public String getNum_usu() {
        return num_usu;
    }

    public void setNum_usu(String num_usu) {
        this.num_usu = num_usu;
    }

    public String getCel_usu() {
        return cel_usu;
    }

    public void setCel_usu(String cel_usu) {
        this.cel_usu = cel_usu;
    }

    public String getEmail_usu() {
        return email_usu;
    }

    public void setEmail_usu(String email_usu) {
        this.email_usu = email_usu;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
